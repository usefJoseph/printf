#include "ft_printf.h"

void	ft_putstr(char *s, int *j)
{
	int	i;

	if (s == NULL)
		ft_putstr("(null)", j);
	i = 0;
	if (s)
	{
		while (s[i])
		{
			ft_putchar(s[i], j);
			i++;
		}
	}
}
