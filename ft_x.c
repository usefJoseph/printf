#include "ft_printf.h"

void	ft_x(unsigned int n, int *j)
{
	char	*s;

	s = "0123456789abcdef";
	if (n >= 16)
	{
		ft_x(n / 16, j);
		ft_x(n % 16, j);
	}
	else
		ft_putchar(s[n], j);
}
